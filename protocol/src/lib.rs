//! # Astrcode 协议定义
//!
//! 本库定义了跨模块通信的协议格式，是整个后端架构的**纯数据契约层**。
//!
//! ## 架构定位
//!
//! `protocol` crate 只负责边界 wire types。
//! 它定义可序列化的数据结构，但不负责宿主侧映射、运行时寻址或传输抽象。
//!
//! ## 核心功能模块
//!
//! - **HTTP DTO** (`http`): API 请求/响应的数据结构，用于 server 与前端之间的序列化通信
//! - **插件协议** (`plugin`): 基于 JSON-RPC 的插件进程通信消息格式，包括握手、能力描述、调用/事件流
//! - **插件能力描述符**: 插件握手与调用所需的能力元数据
//!
//! ## 设计原则
//!
//! - 所有 DTO 都是纯数据，不包含业务逻辑
//! - 使用 serde 进行序列化/反序列化
//! - 版本控制优先通过命名空间管理（如 `conversation::v1`、`plugin::v1`）
//! - 每个线缆命名空间可附带自己的版本常量，但不再以裸历史编号组织目录结构

mod capability;
pub mod http;
pub mod plugin;
